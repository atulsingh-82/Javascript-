
// 1) Data Types in JavaScript
// Data types tell what kind of value a variable stores.
// A. Primitive Data Types (Basic Types)
// Number
// Used for integers and decimal numbers.
// Example:
let age = 20;
let price = 99.5;

// String
// Used for text data.
// Example:
let name = "Atul";

// Boolean
// Has only two values: true or false.
// Example:
let isStudent = true;

// Undefined
// Variable declared but value not assigned.
// Example:
let x;
Null

// Represents an empty value.
// Example:
let data = null;

// BigInt
// Used for very large numbers.
// Example:
let num = 12345678901234567890;

// Symbol
// Used to create unique values.
// Example:
let id = Symbol("id");


// B. Non-Primitive Data Type
// Object – Stores multiple values in key-value form.
// Example:
let student = {
   name: "Atul",
   age: 20
};
